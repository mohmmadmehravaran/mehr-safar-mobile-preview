// لیست نام استان‌ها
export const PROVINCE_NAMES: string[] = Object.keys(PROVINCES);

// شهرهای یک استان
export function getCitiesForProvince(province: string): string[] {
  return PROVINCES[province] || [];
}

// یافتن استانِ یک شهر (اولین تطابق)
export function findProvinceOfCity(city: string): string {
  const n = normalizeFa(city);
  for (const [prov, cities] of Object.entries(PROVINCES)) {
    if (cities.some((c) => normalizeFa(c) === n)) return prov;
  }
  return '';
}

export const BIG_CITIES: string[] = ["تهران", "مشهد", "اصفهان", "شیراز"];

// لیست تمام شهرهای ایران (مشتق‌شده از PROVINCES، یکتا) — برای انتخابگر شهر در جستجوی هتل
export const IRAN_CITIES: string[] = Array.from(
  new Set(Object.values(PROVINCES).flat())
);

// نرمال‌سازی متن فارسی برای جستجو (یکسان‌سازی ی/ک، حذف فاصله و نیم‌فاصله اضافی)
export function normalizeFa(s: string): string {
  return (s || '')
    .replace(/[\u064A\u0649]/g, '\u06CC') // ي/ى → ی
    .replace(/\u0643/g, '\u06A9')        // ك → ک
    .replace(/[\u200c\s]+/g, ' ')         // نیم‌فاصله/فاصله‌ها → یک فاصله
    .trim()
    .toLowerCase();
}

// جستجوی شهرها بر اساس متن ورودی
export function searchCities(query: string, limit = 50): string[] {
  const q = normalizeFa(query);
  if (!q) return [];
  const starts: string[] = [];
  const contains: string[] = [];
  for (const c of IRAN_CITIES) {
    const n = normalizeFa(c);
    if (n.startsWith(q)) starts.push(c);
    else if (n.includes(q)) contains.push(c);
  }
  return [...starts, ...contains].slice(0, limit);
}
